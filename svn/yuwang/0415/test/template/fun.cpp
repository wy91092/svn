#include<string>
#include<iostream>
using namespace std;
template <class T>
T Max(T a, T b)
{
  return a>b ? a: b;
}
int main()
{
  int i1=2,i2=5;
  double d1=3.2,d2=4.3;
  string str1("haha"),str2("hehe");
  cout<<"int :" <<Max(i1,i2)<<endl;
  cout <<"double: "<<Max(d1,d2)<<endl;
  cout<<"string: "<<Max(str1,str2)<<endl;
  return 0;
}
