#include"complex.h"
Complex Complex::operator + (Complex &c)
{
  Complex temp;
  temp.real =real +c.real;
  temp.imag =imag +c.imag;
  return temp;
}
Complex Complex::operator + (double d)
{
  Complex temp;
  temp.real=real+d;
  temp.imag=imag;
  return temp;
}
Complex Complex::operator = (Complex c)
{
 real=c.real;
 imag=c.imag;
 return *this;
}

